	<p>Dead Poets Society is a 1989 American drama film directed by Peter Weir, written by Tom Schulman, and starring Robin Williams. Set in 1959 at the fictional elite conservative Vermont boarding school Welton Academy, it tells the story of an English teacher who inspires his students through his teaching of poetry.</p>
	<p>The film received critical acclaim and was a box office success. It won the BAFTA Award for Best Film, the César Award for Best Foreign Film and the David di Donatello Award for Best Foreign Film. Schulman received an Academy Award for Best Original Screenplay for his work. It is considered to be one of the most inspirational films ever made.</p>
	<p>Throughout the film, there is a motif of seizing the day and making one’s life extraordinary. Instead of succumbing to life’s set ways, one has the power to transcend and create a life of their own. The film has been able to stand the test of time and is considered a classic, and has sprouted the creation of books, plays, and parodies based on it. In short, the film tells the story of English professor John Keating, who inspires his students at Welton Academy to a love of poetry and teaches them to overcome their reluctance to make changes in their lives.</p>

	<h2>Plot</h2>
	
	<figure>
	
		<img src="images/dpsimage.jpg" alt="Dead Poets Society image">
		<figcaption class="ab">Dead Poets Society image</figcaption>
		
	</figure>
	
	<p>In the autumn of 1959, shy Todd Anderson begins his senior year of high school at Welton Academy, an all-male, elite prep school. He is assigned one of Welton's most promising students, Neil Perry, as his roommate and is quickly accepted by Neil's friends: Knox Overstreet, Richard Cameron, Steven Meeks, Gerard Pitts, and Charlie Dalton.</p>
	<p>On the first day of classes for the year, they are surprised by the unorthodox teaching methods of the new English teacher John Keating, an alumnus of Welton who encourages his students to "make your lives extraordinary", a sentiment he summarizes with the Latin expression carpe diem, meaning "seize the day."</p>
	<p>Subsequent lessons include having them take turns standing on his desk to demonstrate ways to look at life in a different way, telling them to rip out the introduction of their poetry books which explains a mathematical formula used for rating poetry, and inviting them to make up their own style of walking in a courtyard to encourage them to be individuals. His methods attract the attention of strict headmaster Gale Nolan.</p>
	<p>Upon learning that Keating was a member of the unsanctioned Dead Poets Society while he was at Welton, Neil restarts the club and he and his friends sneak off campus to a cave where they read poetry and verse, including their own compositions. As the school year progresses, Keating's lessons and their involvement with the club encourage them to live their lives on their own terms. Knox pursues Chris Noel, an attractive cheerleader who is dating Chet Danburry, a football player from a local public school whose family is friends with his.</p>
	<p>Neil discovers his love of acting and gets the role as Puck in a local production of A Midsummer Night's Dream, despite the fact that his domineering father wants him in the Ivy League (and ultimately medical school). Keating helps Todd come out of his shell and realize his potential when he takes him through an exercise in self-expression, resulting in his composing a poem spontaneously in front of the class.</p>
	<p>However, Charlie takes things too far when he publishes an article in the school newspaper in the club's name demanding that girls be admitted to Welton. Nolan paddles Charlie to coerce him into revealing who else is in the Dead Poets Society, but he resists. Nolan also speaks with Keating, warning him that he should discourage his students from questioning authority. Keating does admonish the boys (in his manner), warning that one must assess all consequences.</p>
	<p>Neil's father discovers his son's involvement in the play and forces him to quit on the eve of the opening performance. Devastated, Neil goes to Keating, who advises him to stand his ground and prove to his father that his love of acting is something he takes seriously. Neil's father unexpectedly shows up at the performance. He takes Neil home and says he has been withdrawn from Welton, only to be enrolled in a military academy to prepare him for Harvard so he will become a doctor. Unable to find the courage to stand up to his father, and lacking any support from his concerned mother, a distraught Neil commits suicide.</p>
	<p>Nolan investigates Neil's death at the request of the Perry family. Cameron blames Neil's death on Keating to escape punishment for his own participation in the Dead Poets Society, and names the other members. Confronted by Charlie, Cameron urges the rest of them to let Keating take the fall. Charlie punches Cameron and is expelled. Each of the boys is called to Nolan's office to sign a letter attesting to the truth of Cameron's allegations, even though they know they are false. When Todd's turn comes, he is reluctant to sign, but does so after seeing that the others have complied and succumbing to his parents' pressure.</p>
	<p>Keating is fired and Nolan (who taught English at Welton before becoming headmaster) takes over teaching the class, with the intent of adhering to traditional Welton rules. Keating interrupts the class to collect personal articles; before he leaves, Todd shouts that all of them were forced to sign the letter that resulted in his dismissal and that Neil's death was not his fault. Todd stands on his desk and salutes Keating with the words "O Captain! My Captain!". Knox, Gerard, Steven, and over half of the class do the same, defying a furious Nolan's orders to sit down. Keating is deeply touched by their gesture. He thanks the boys and departs.</p>

	<h2>Production and Development</h2>
	
	
	<p>The original script was written by Tom Schulman, based on his experiences at the Montgomery Bell Academy in Nashville, Tennessee, particularly with his inspirational teacher Samuel Pickering. In Schulman's manuscript, Keating had been ill, slowly dying of Hodgkin lymphoma with a scene showing him on his deathbed in hospital. This was removed by Weir which deemed it unnecessary claiming this would focus audiences on Keating's illness and not on what he stood for.</p>
	<p>Early notes on the script from Disney also suggested making the boy's passion dancing rather than poetry as well as a new title "Sultans of Swing" focusing on the character of Mr. Keating rather than the boys themselves, but both were dismissed outright. In the end, the boy’s passion was poetry.</p>
	<p>Filming started in the winter of 1988 and took place at St. Andrew's School in Middletown, Delaware, and at locations in New Castle, Delaware, and in nearby Wilmington, Delaware. During the shooting, Weir requested the young cast not to use modern slang, even off camera. This was to make the film more authentic and to have the cast embody their characters.</p>
