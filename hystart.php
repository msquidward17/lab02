		<h2>Gameplay</h2>
		
		<p>Players explore the large interconnected world of Hallownest using a variety of movement and combat techniques. In classic Metroidvania style, there are areas of the world that the player cannot access until they obtain particular items or abilities. Maps for each area must be bought from the cartographer, and the player can also buy upgrades that allow them to reveal parts of the map as they explore. </p>
		
		<p>Many areas have a 'boss' enemy that the player must defeat in order to continue, and a number of sub-bosses that drop useful rewards or upgrades. Defeated enemies drop Geo, a currency that the player can use to buy items from shopkeepers. If the player is killed, they respawn at the last place they rested and lose all their Geo. </p>

		<p>They must then return to their corpse and defeat a ghost in order to recover the currency. The player also earns 'Soul' by attacking enemies, which they can then spend to recover lost health or activate special abilities. Health can also be recovered at hot springs, which are located around the world, or by resting at a bench. </p>

		<h2>Content Packs</h2>
		
		<p>Hidden Dreams is the first of the four free content packs for Hollow Knight. It was released on the 3rd of August, 2017. The main content focuses on bringing in "two of Hallownest’s greatest warriors", along with the first major quality of life changes including a new Stag Station and a new fast travel mechanic. </p>
		

		<p>The Grimm Troupe is the second of the four free content packs for Hollow Knight. It was released on the 26th of October, 2017.In this expansion, a mysterious group of circus performers known as the Grimm Troupe arrives in Dirtmouth. Their leader, Troupe Master Grimm, asks the Knight to take part in a "twisted ritual.” Eventually the Knight must choose to either complete the ritual by fighting Nightmare King Grimm, or to banish the Troupe from Hallownest. </p>
		
		<figure>
		<img src="images/grimm.jpg" alt="grimm update poster">
