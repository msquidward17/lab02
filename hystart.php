		<p>Lifeblood is the third free content pack to Hollow Knight. It was officially released on 20 April, 2018. As it was not one of the three originally planned content packs, it did not release with a teaser. Lifeblood focused mainly on enhancing technical aspects of the game, bringing upgrades and optimizations of various kinds. </p>

		<p>Godmaster, previously named Gods & Glory, is the fourth and last free content pack for Hollow Knight. It is the final chapter of the Knight's story. The free content pack features all-new characters, new quests, secrets, and new end-game bosses. It also includes a new game mode, more Delicate Flower recipients, and new music along with some remixes. </p>
